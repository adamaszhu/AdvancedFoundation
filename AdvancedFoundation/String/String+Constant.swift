/// String+Constant defines some common string constant.
///
/// - author: Adamas
/// - version: 1.5.0
/// - date: 23/03/2019
public extension String {
    
    // Constants
    static let space = " "
    static let linebreak = "\n"
    static let empty = ""
    static let plus = "+"
    static let dot = "."
    static let tab = "\t"
    static let comma = ","
    static let dash = "-"
    static let comment = "//"
    static let leftSquareBracket = "["
    static let rightSquareBracket = "]"
    static let forwardSlash = "/"
}
